// console.log('BEFORE CONDITIONAL');
let random = Math.random();
if (random < 0.5) {
    console.log('YOUR RANDOM NUMBER IS LESS THAN 0.5');
    console.log(random);
}
if (random >= 0.5) {
    console.log('YOUR RANDOM NUMBER IS BIGGER THAN 0.5');
    console.log(random);
}
